#include <bits/stdc++.h>
using namespace std;
// all the if statement is to be check when use multiple if clause
int main()
{
    int a, b;
    cout<<"\n Enter the a & b value are : ";
    cin>>a>>b;
    
    if(a>b){
        cout<<"\n a is greater than b.";
    }
    if(a<b){
        cout<<"\n b is greater than a.";
    }
    if(a==b){
        cout<<"\n a & b are equal.";
    }
    return 0;
}
